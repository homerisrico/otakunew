    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validateData = $request->validate([
            'imagen' => 'required|image|mimes:jpeg,png',
        ]);
        if($request->hasFile('imagen')){
            $file = $request->file('imagen');
            $name = time().$file->getClientOriginalName();
            $file->move(public_path().'/images/imagenesArticulo/',$name);
        }
        $imagen = new Imagen();
        $imagen->imagen = $name;
        $imagen->articulo_id = $request->articulo_id;
        $imagen->save();

        $imgid = array("nombre" => $name);
        echo json_encode($imgid);
        // return redirect()->route('showarticle');
    }

